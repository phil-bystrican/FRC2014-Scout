    /*takes the event as a parameter. Requests a match number, validates the input and opens the modify match jframe. returns nothing*/
    private void btn_modifyGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifyGameActionPerformed
        //show the user a dialog box to get their imput
        String str_inputValue = JOptionPane.showInputDialog("Input the match number to modify:");

        //if nothing was inputed
        if (str_inputValue == null) {
            //do nothing
            return;
        }
        try {
            //if the parsed integer is 0
            if (Integer.parseInt(str_inputValue) == 0) {
                //get the input again
                btn_modifyGameActionPerformed(null);
            } else {
                //initialize the modify team jframe
                ModifyMatchScoring jfrm_modifyMatch = new ModifyMatchScoring(Integer.parseInt(str_inputValue));
                //display the jframe
                super.pushFrame(jfrm_modifyMatch, this);
                //call the frame's became visible method which checks if the entered team exists
                jfrm_modifyMatch.becameVisible();
            }
        } catch (NumberFormatException ex) {
            //an improper integer such as a letter was entered
            //get the input again
            btn_modifyGameActionPerformed(null);
        }
    }//GEN-LAST:event_btn_modifyGameActionPerformed

