    /*takes the event as a parameter. Requests a team number, validates the 
     * input and opens the modify team jframe. returns nothing*/
    private void btn_modifyTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifyTeamActionPerformed
        //show the user a dialog box to get their imput
        String str_inputValue = 
                JOptionPane.showInputDialog("Input the team number to modify:");

        //if nothing was inputed
        if (str_inputValue == null) {
            //cancel was pressed
            return;
        }
        try {
            //if the parsed integer is 0
            if (Integer.parseInt(str_inputValue) == 0) {
                //get the input again
                btn_modifyTeamActionPerformed(null);
            } else {
                //initialize the modify team jframe
                ModifyTeam jfrm_modifyTeam = 
                        new ModifyTeam(Integer.parseInt(str_inputValue), false);
                //display the jframe
                super.pushFrame(jfrm_modifyTeam, this);
                //call the frame's became visible method which checks if the entered team exists
                jfrm_modifyTeam.becameVisible();
            }
        } catch (NumberFormatException ex) {
            //an improper integer such as a letter was entered
            //get the input again
            btn_modifyTeamActionPerformed(null);
        }
    }//GEN-LAST:event_btn_modifyTeamActionPerformed

